public class H函数 {


    // 函数（方法）的格式static(表示该方法属于类，而不是属于对象) void 函数名()
    static void sing() {
        System.out.print("我一路向北，");
        System.out.print("离开有你的季节，");
        System.out.print("你说你好累，");
        System.out.println("已无法再爱上谁。");
        System.out.print("风在山路吹，");
        System.out.print("过往的画面全都是不对，");
        System.out.print("细数惭愧，");
        System.out.println("我伤你几回。");
    }
    public static void main(String[] args) {
        sing();
    }
}